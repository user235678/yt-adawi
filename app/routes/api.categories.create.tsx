import { json, type ActionFunction } from "@remix-run/node";
import { readToken } from "~/utils/session.server";

export const action: ActionFunction = async ({ request }) => {
    if (request.method !== "POST") {
        return json({ success: false, error: "M√©thode non autoris√©e" }, { status: 405 });
    }

    try {
        const token = await readToken(request);
        
        if (!token) {
            return json({ success: false, error: "Non authentifi√©" }, { status: 401 });
        }

        const formData = await request.formData();
        const name = formData.get("name") as string;
        const description = formData.get("description") as string;
        const parent_id = formData.get("parent_id") as string;

        // Validation
        if (!name || !description) {
            return json({ 
                success: false, 
                error: "Le nom et la description sont requis" 
            }, { status: 400 });
        }

        // Pr√©parer les donn√©es pour l'API
        const categoryData: any = {
            name: name.trim(),
            description: description.trim(),
        };

        // Ajouter parent_id seulement s'il est fourni et non vide
        if (parent_id && parent_id.trim() !== '') {
            categoryData.parent_id = parent_id.trim();
        }

        console.log("üì§ Cr√©ation cat√©gorie:", categoryData);

        // Envoyer √† l'API backend
        const response = await fetch("https://showroom-backend-2x3g.onrender.com/products/categories/", {
            method: "POST",
            headers: {
                "Authorization": `Bearer ${token}`,
                "Content-Type": "application/json",
            },
            body: JSON.stringify(categoryData),
        });

        if (!response.ok) {
            const errorData = await response.json().catch(() => ({}));
            console.error("‚ùå Erreur cr√©ation cat√©gorie:", response.status, errorData);
            
            return json({ 
                success: false, 
                error: errorData.detail || `Erreur ${response.status}` 
            }, { status: response.status });
        }

        const createdCategory = await response.json();
        
        console.log("‚úÖ Cat√©gorie cr√©√©e:", createdCategory);
        
        return json({ 
            success: true, 
            category: createdCategory,
            message: "Cat√©gorie cr√©√©e avec succ√®s"
        });

    } catch (error) {
        console.error("‚ùå Erreur lors de la cr√©ation de la cat√©gorie:", error);
        return json({ 
            success: false, 
            error: "Erreur de connexion au serveur" 
        }, { status: 500 });
    }
};
